CREATE TABLE matches
(
  id bigserial NOT NULL,
  match_date timestamp without time zone NOT NULL,
  match_day date NOT NULL,
  team1 character varying(100) NOT NULL,
  team2 character varying(100) NOT NULL,
  closed boolean default false,
  comments character varying(100),
  CONSTRAINT matches_pkey PRIMARY KEY (id),
  CONSTRAINT team1_fkey FOREIGN KEY (team1)
      REFERENCES teams (team) MATCH SIMPLE
      ON UPDATE NO ACTION ON DELETE NO ACTION,
  CONSTRAINT team2_fkey FOREIGN KEY (team2)
      REFERENCES teams (team) MATCH SIMPLE
      ON UPDATE NO ACTION ON DELETE NO ACTION
)
WITH (
  OIDS=FALSE
);
ALTER TABLE matches
  OWNER TO postgres;
  
CREATE INDEX matches_idx ON matches (match_day, team1, team2);

