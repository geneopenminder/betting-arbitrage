CREATE TABLE bets
(
  id bigserial NOT NULL,
  match_id bigint not null,
  bet character varying(20) NOT NULL,
  bet_val numeric NOT NULL,
  site character varying(100) NOT NULL,
  date_added timestamp without time zone default now() NOT NULL,
  updated timestamp without time zone NOT NULL,
  CONSTRAINT bets_pkey PRIMARY KEY (id),
  CONSTRAINT match_fkey FOREIGN KEY (match_id)
      REFERENCES matches (id) MATCH SIMPLE
      ON UPDATE NO ACTION ON DELETE NO ACTION,
  CONSTRAINT bet_type_fkey FOREIGN KEY (bet)
      REFERENCES bet_types (bet) MATCH SIMPLE
      ON UPDATE NO ACTION ON DELETE NO ACTION,
  CONSTRAINT site_fkey FOREIGN KEY (site)
      REFERENCES bet_sites (site) MATCH SIMPLE
      ON UPDATE NO ACTION ON DELETE NO ACTION
)
WITH (
  OIDS=FALSE
);
ALTER TABLE bets
  OWNER TO postgres;
  
